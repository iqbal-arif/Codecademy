// Today's Forecast in Kelvin

const kelvin = 293;

// Converting Kelvin to Celsius

let celsius;
celsius = kelvin - 273;

// Converting Celsius to Fahrenheit

let fahrenheit = celsius * (9 / 5) + 32;

// Rounding down the decimal number

temperature = Math.floor(fahrenheit);

console.log(`The temperature is ${temperature} degree Fahrenheit`);
